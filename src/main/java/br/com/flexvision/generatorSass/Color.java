package br.com.flexvision.generatorSass;

public class Color {

	private String hex;
	private Boolean dark;
	
	public String getHex() {
		return hex;
	}
	public void setHex(String hex) {
		this.hex = hex;
	}
	public Boolean getDark() {
		return dark;
	}
	public void setDark(Boolean dark) {
		this.dark = dark;
	}
	
}
